package com.tcs.contentorganizer.beans;

public class FileShareBean {

	String fileShareName;
	String fileSharePath = "";
	String fileShareStatus = "";
	String fileShareCoreName = "";

	public String getFileShareName() {
		return fileShareName;
	}

	public void setFileShareName(String fileShareName) {
		this.fileShareName = fileShareName;
	}

	public String getFileSharePath() {
		return fileSharePath;
	}

	public void setFileSharePath(String fileSharePath) {
		this.fileSharePath = fileSharePath;
	}

	public String getFileShareStatus() {
		return fileShareStatus;
	}

	public void setFileShareStatus(String fileShareStatus) {
		this.fileShareStatus = fileShareStatus;
	}

	public String getFileShareCoreName() {
		return fileShareCoreName;
	}

	public void setFileShareCoreName(String fileShareCoreName) {
		this.fileShareCoreName = fileShareCoreName;
	}

}
